import type { TextElement } from "@/lib/timeline";
import { DEFAULT_NEW_ELEMENT_DURATION } from "@/lib/timeline/creation";

export type TextPresetCategory =
	| "basic"
	| "title"
	| "subtitle"
	| "lower-third"
	| "callout"
	| "quote"
	| "social"
	| "bold"
	| "handwritten"
	| "neon";

export interface TextPreset {
	id: string;
	type: string;
	name: string;
	keywords: string[];
	category: TextPresetCategory;
	build: () => Omit<TextElement, "id" | "startTime">;
}

const baseDefaults = {
	duration: DEFAULT_NEW_ELEMENT_DURATION,
	trimStart: 0,
	trimEnd: 0,
	textAlign: "center" as const,
	fontWeight: "normal" as const,
	fontStyle: "normal" as const,
	textDecoration: "none" as const,
	letterSpacing: 0,
	lineHeight: 1.2,
	hidden: false,
	transform: {
		scaleX: 1,
		scaleY: 1,
		position: { x: 0, y: 0 },
		rotate: 0,
	},
	opacity: 1,
};

/** No background box (the common case). */
const NO_BG = {
	enabled: false,
	color: "#000000",
	cornerRadius: 0,
	paddingX: 0,
	paddingY: 0,
	offsetX: 0,
	offsetY: 0,
};

/** A filled, padded background box (for pills, captions, stickers). */
function box(color: string, cornerRadius: number) {
	return {
		enabled: true,
		color,
		cornerRadius,
		paddingX: 16,
		paddingY: 8,
		offsetX: 0,
		offsetY: 0,
	};
}

