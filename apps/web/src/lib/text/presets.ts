/** Compact authoring helper: one real, named preset (no auto-numbers). */
function tp(
	id: string,
	name: string,
	category: TextPresetCategory,
	content: string,
	style: Partial<Omit<TextElement, "id" | "startTime" | "type" | "name">>,
	keywords: string[] = [],
): TextPreset {
	return {
		id,
		type: id,
		name,
		keywords: [category, ...keywords],
		category,
		build: () => ({
			...baseDefaults,
			type: "text",
			name,
			content,
			fontSize: 18,
			fontFamily: "Inter",
			color: "#ffffff",
			background: NO_BG,
			...style,
		}),
	};
}

